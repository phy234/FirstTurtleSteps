from turtle import *

def flowerdraw(a = ("orange")):
    pencolor("brown")
    pensize(2.5)
    fillcolor(a)
    begin_fill()
    lt(60)
    fd(80)
    lt(60)
    fd(80)
    lt(60)
    fd(80)
    lt(60)
    fd(80)
    lt(60)
    fd(80)
    lt(60)
    fd(80)
    end_fill()

